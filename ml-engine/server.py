def generate_fallback_report(predictions, role):
    """Generate a structured report from YOLO detections when Gemini is unavailable."""
    if not predictions:
        if role == 'patient':
            return "**Good news!** Our AI scan did not detect any major concerns in your dental image. Keep up your great oral hygiene routine!\n\n**Preventive Plan**\n- Brush twice daily with fluoride toothpaste\n- Floss every evening\n- Visit your dentist every 6 months for a routine checkup"
        else:
            return "**Clinical Summary**\nNo significant pathological findings detected by the AI diagnostic system in this image.\n\n**Preventive Plan**\n- Continue routine monitoring\n- Schedule standard 6-month recall\n- Reinforce home care instructions with patient"

    # Count and categorize findings
    counts = {}
    for p in predictions:
        label = p['class']
        counts[label] = counts.get(label, 0) + 1

    findings_text = ', '.join([f"{v}x {k}" for k, v in counts.items()])
    avg_conf = sum(p['confidence'] for p in predictions) / len(predictions)

    if role == 'patient':
        report = f"**Your Dental Scan Summary**\n\nOur AI system has completed your scan and identified some areas to keep an eye on: **{findings_text}**.\n\nDon't worry — early detection means easier treatment! Your dentist will guide you on the best next steps.\n\n**Preventive Plan**\n- Schedule an appointment with your dentist soon to discuss these findings\n- Brush gently twice a day and use fluoride toothpaste\n- Avoid sugary foods and drinks between meals\n- Floss daily to prevent gum issues\n\n*This report was generated by the AI detection system (confidence: {avg_conf*100:.0f}%).*"
    else:
        lines = ["**Clinical AI Analysis Report**\n"]
        lines.append(f"**Detected Findings:** {findings_text}")
        lines.append(f"**Average Detection Confidence:** {avg_conf*100:.1f}%\n")
        lines.append("**Findings Breakdown:**")
        for label, count in counts.items():
            relevant = [p for p in predictions if p['class'] == label]
            avg = sum(p['confidence'] for p in relevant) / len(relevant)
            lines.append(f"- **{label}**: {count} instance(s) detected (avg. confidence: {avg*100:.0f}%)")
        lines.append("\n**Preventive Plan**")
        if any('caries' in k.lower() or 'cavity' in k.lower() for k in counts):
            lines.append("- Caries detected: Consider excavation and restoration. Evaluate for pulp involvement.")
        if any('gum' in k.lower() or 'swelling' in k.lower() for k in counts):
            lines.append("- Gingival inflammation noted: Recommend scaling, root planing, and home care reinforcement.")
        if any('missing' in k.lower() for k in counts):
            lines.append("- Missing tooth: Discuss prosthetic options — implant, bridge, or partial denture.")
        if any('discolor' in k.lower() for k in counts):
            lines.append("- Discoloration present: Assess for intrinsic vs extrinsic cause. Consider bleaching or veneer.")
        lines.append("- Schedule follow-up in 4–6 weeks to monitor treatment response.")
        lines.append(f"\n*Note: Full AI narrative report unavailable — API quota limit reached. Detection results are from the YOLOv8 model at {avg_conf*100:.0f}% average confidence.*")
        report = '\n'.join(lines)

    return report


